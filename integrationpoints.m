function [rsIP] = integrationpoints(scheme,cont_disc)

if (cont_disc == 1) % quadrilateral (continuum)
    
    % Gauss
    if (scheme == 2)
        
        rsIP  =  [  -sqrt(1/3) -sqrt(1/3); ...
                    -sqrt(1/3)  sqrt(1/3); ...
                     sqrt(1/3) -sqrt(1/3); ...
                     sqrt(1/3)  sqrt(1/3)];
        
        wIP = ones(4,1);
        
        rsIP = [rsIP wIP];
        
    elseif (scheme == 3)
        
        rsIP  =  [  -sqrt(3/5)  -sqrt(3/5); ...
                    -sqrt(3/5)   0        ; ...
                    -sqrt(3/5)   sqrt(3/5); ...
                    0           -sqrt(3/5); ...
                    0            0        ; ...
                    0            sqrt(3/5); ...
                    sqrt(3/5)   -sqrt(3/5); ...
                    sqrt(3/5)    0        ; ...
                    sqrt(3/5)    sqrt(3/5)];
        
        wIP =    [  (5/9)*(5/9); ...
                    (5/9)*(8/9); ...
                    (5/9)*(5/9); ...
                    (8/9)*(5/9); ...
                    (8/9)*(8/9); ...
                    (8/9)*(5/9); ...
                    (5/9)*(5/9); ...
                    (5/9)*(8/9); ...
                    (5/9)*(5/9)];
        
        rsIP = [rsIP wIP];
        
    elseif (scheme == 4)
        
        rsIP  =  [  -sqrt((3+2*sqrt(6/5))/7) -sqrt((3+2*sqrt(6/5))/7); ...
                    -sqrt((3+2*sqrt(6/5))/7) -sqrt((3-2*sqrt(6/5))/7); ...
                    -sqrt((3+2*sqrt(6/5))/7)  sqrt((3-2*sqrt(6/5))/7); ...
                    -sqrt((3+2*sqrt(6/5))/7)  sqrt((3+2*sqrt(6/5))/7); ...
                    -sqrt((3-2*sqrt(6/5))/7) -sqrt((3+2*sqrt(6/5))/7); ...
                    -sqrt((3-2*sqrt(6/5))/7) -sqrt((3-2*sqrt(6/5))/7); ...
                    -sqrt((3-2*sqrt(6/5))/7)  sqrt((3-2*sqrt(6/5))/7); ...
                    -sqrt((3-2*sqrt(6/5))/7)  sqrt((3+2*sqrt(6/5))/7); ...
                     sqrt((3-2*sqrt(6/5))/7) -sqrt((3+2*sqrt(6/5))/7); ...
                     sqrt((3-2*sqrt(6/5))/7) -sqrt((3-2*sqrt(6/5))/7); ...
                     sqrt((3-2*sqrt(6/5))/7)  sqrt((3-2*sqrt(6/5))/7); ...
                     sqrt((3-2*sqrt(6/5))/7)  sqrt((3+2*sqrt(6/5))/7); ...
                     sqrt((3+2*sqrt(6/5))/7) -sqrt((3+2*sqrt(6/5))/7); ...
                     sqrt((3+2*sqrt(6/5))/7) -sqrt((3-2*sqrt(6/5))/7); ...
                     sqrt((3+2*sqrt(6/5))/7)  sqrt((3-2*sqrt(6/5))/7); ...
                     sqrt((3+2*sqrt(6/5))/7)  sqrt((3+2*sqrt(6/5))/7)];
        
        wIP =    [  ((18-sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18+sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18-sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18+sqrt(30))/36); ...
                    ((18-sqrt(30))/36)*((18-sqrt(30))/36)];
        
        rsIP = [rsIP wIP];
        
    end
    
elseif (cont_disc == 2) % interface
    
    % Newton-Cotes
    if (scheme == 2) % trapezoid rule
        
        rsIP  =  [ -1; 1];
        
        wIP = (1+1)*[0.5; 0.5];
        
        rsIP = [rsIP wIP];
        
    elseif (scheme == 3) % Simpson rule
        
        rsIP  =  [ -1; 0; 1];
        
        wIP = (1+1)*[1/6; 4/6; 1/6];
        
        rsIP = [rsIP wIP];
     
    end
    
end